# EMA实时计算
def calc_ema(prices, period):
    multi = 2 / (period + 1)
    ema = [sum(prices[:period]) / period]
    for p in prices[period:]:
        ema.append((p - ema[-1]) * multi + ema[-1])
    return ema[-1]

def get_ema(symbol):
    try:
        resp = requests.get('https://api.gateio.ws/api/v4/futures/usdt/candlesticks',
            params={'contract': symbol, 'interval': '4h', 'limit': 200}, timeout=15)
        data = resp.json()
        closes = [float(k[4]) for k in data]
        return calc_ema(closes,10), calc_ema(closes,20), calc_ema(closes,50), calc_ema(closes,200)
    except:
        return None, None, None, None

